interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
  onClose: () => void;
  position?: "top" | "bottom";
  align?: "left" | "right";
}

export default function EmojiPicker({ onSelect, onClose, position = "top", align = "left" }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState(Object.keys(EMOJI_CATEGORIES)[0]);
  const [search, setSearch] = useState("");

  const filtered = search.trim()
    ? Object.values(EMOJI_CATEGORIES).flat().filter((e) =>
        // simple substring search in the flat list
        e.includes(search.trim())
      )
    : EMOJI_CATEGORIES[activeCategory as keyof typeof EMOJI_CATEGORIES] || [];

  return (
    <div
      onClick={(e) => e.stopPropagation()}
      className={`absolute z-50 bg-slate-900 border border-slate-700 rounded-2xl shadow-2xl w-72 overflow-hidden
        ${position === "top" ? "-bottom-2" : "top-0"}
        ${align === "right" ? "right-0" : "left-0"}
      `}
    >
      {/* Search */}
      <div className="p-2 border-b border-slate-700">
        <input
          type="text"
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          placeholder="Search emoji..."
          autoFocus
          className="w-full bg-slate-800 rounded-xl px-3 py-2 text-sm text-white outline-none placeholder-slate-500 focus:ring-1 focus:ring-blue-500"
        />
      </div>

      {/* Category tabs */}
      {!search && (
        <div className="flex overflow-x-auto border-b border-slate-700 px-1 py-1 gap-0.5 scrollbar-hide">
          {Object.keys(EMOJI_CATEGORIES).map((cat) => (
            <button
              key={cat}
              onClick={() => setActiveCategory(cat)}
              title={cat}
              className={`shrink-0 px-2 py-1 rounded-lg text-sm transition cursor-pointer ${
                activeCategory === cat
                  ? "bg-blue-600 text-white"
                  : "text-slate-400 hover:bg-slate-800 hover:text-white"
              }`}
            >
              {cat.split(" ")[0]}
            </button>
          ))}
        </div>
      )}

      {/* Emoji grid */}
      <div className="h-48 overflow-y-auto p-2">
        <div className="grid grid-cols-8 gap-0.5">
          {filtered.map((emoji, i) => (
            <button
              key={`${emoji}-${i}`}
              onClick={() => { onSelect(emoji); onClose(); }}
              className="flex items-center justify-center w-8 h-8 rounded-lg text-xl hover:bg-slate-700 transition cursor-pointer"
              title={emoji}
            >
              {emoji}
            </button>
          ))}
          {filtered.length === 0 && (
            <p className="col-span-8 text-center text-slate-500 text-sm py-6">No emoji found</p>
          )}
        </div>
      </div>
    </div>
  );
}

